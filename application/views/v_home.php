<script>
    $(document).ready(function() {
        $('#kategori, #durasi, #layanan').on('change', function() {
            var kategori = $('#kategori').val();
            var durasi = $('#durasi').val();
            var layanan = $('#layanan').val();

            if (kategori && durasi && layanan) {
                $.ajax({
                    type: 'POST',
                    url: '<?= base_url('home/get_harga'); ?>',
                    data: {
                        kategori: kategori,
                        durasi: durasi,
                        layanan: layanan
                    },
                    success: function(data) {
                        var harga = parseInt(data);
                        var formattedHarga = harga.toLocaleString('id-ID', {
                            style: 'currency',
                            currency: 'IDR',
                            minimumFractionDigits: 0
                        });
                        $('#harga').text(formattedHarga);
                    }
                });
            } else {
                $('#harga').text('Silakan pilih semua opsi');
            }
        });
    });
</script>
